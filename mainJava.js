function changeColor(bgColor){
  bgColor.style.backgroundColor = "rgba(37, 79, 172, 0.48)";
}

function changeColorG(bgColor){
  bgColor.style.backgroundColor = "rgba(37, 79, 172, 0.48)";
  bgColor.style.fontSize = "large";
}

function defaultColor(bgColor){
  bgColor.style.backgroundColor = "transparent";
  bgColor.style.fontSize = "medium";
}
